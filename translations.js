// Language switching function
let currentLang = 'en';

function switchLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;

  // Save language preference to localStorage
  localStorage.setItem('preferredLanguage', lang);

  // Update active state on language buttons
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });

  // Translate all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const [section, subkey] = key.split('.');

    if (translations[section] && translations[section][lang] && translations[section][lang][subkey]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[section][lang][subkey];
      } else {
        el.innerHTML = translations[section][lang][subkey];
      }
    }
  });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  // Check for saved language preference, default to 'en' if none found
  const savedLang = localStorage.getItem('preferredLanguage') || 'en';
  switchLanguage(savedLang);
});
